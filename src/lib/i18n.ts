export type Dict = typeof es;

export const dictionaries: Record<Locale, Dict> = { es, en };

export function getDictionary(locale: Locale): Dict {
  return dictionaries[locale] ?? dictionaries[DEFAULT_LOCALE];
}

export function normalizeLocale(value: string | undefined | null): Locale {
  return value === "en" || value === "es" ? value : DEFAULT_LOCALE;
}

/**
 * Build the localized "your limits" explanation shown on each ballot, using the
 * structured eligibility fields (so we never depend on the engine's English text).
 */
export function explainEligibility(
  dict: Dict,
  method: string,
  e: { minSelections: number; maxSelections: number | null; exactSelections: number | null; voteWeight: number; scoreMin: number; scoreMax: number },
  groupName?: string
): string {
  const isEs = dict === dictionaries.es;
  const groupClause = groupName
    ? isEs
      ? `Estás en el grupo ${groupName}, por lo que `
      : `You are in the ${groupName} group, so `
    : "";
  const weightNote =
    e.voteWeight !== 1 ? (isEs ? ` Tu voto cuenta como ${e.voteWeight}.` : ` Your vote counts as ${e.voteWeight}.`) : "";

  switch (method) {
    case "SINGLE":
      return isEs ? `${groupClause}puedes elegir exactamente una opción.` : `${groupClause}you can select exactly one option.`;
    case "RANKED":
      return isEs
        ? "Ordena tus opciones preferidas en orden de preferencia."
        : "Rank your preferred options in order of preference.";
    case "SCORE":
      return isEs
        ? `${groupClause}da a cada opción una puntuación de ${e.scoreMin} a ${e.scoreMax}.`
        : `${groupClause}give each option a score from ${e.scoreMin} to ${e.scoreMax}.`;
    default: {
      if (e.exactSelections != null)
        return isEs
          ? `${groupClause}debes seleccionar exactamente ${e.exactSelections} opción(es).`
          : `${groupClause}you must select exactly ${e.exactSelections} option(s).`;
      const cap =
        e.maxSelections == null
          ? isEs
            ? "cualquier número de"
            : "any number of"
          : isEs
            ? `hasta ${e.maxSelections}`
            : `up to ${e.maxSelections}`;
      const minPart =
        e.minSelections > 0 ? (isEs ? ` (al menos ${e.minSelections})` : ` (at least ${e.minSelections})`) : "";
      return isEs
        ? `${groupClause}puedes seleccionar ${cap} opción(es)${minPart}.${weightNote}`
        : `${groupClause}you can select ${cap} option(s)${minPart}.${weightNote}`;
    }
  }
}
